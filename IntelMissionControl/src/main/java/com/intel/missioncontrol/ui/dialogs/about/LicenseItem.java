package com.intel.missioncontrol.ui.dialogs.about;

class LicenseItem {
    private final String name;
    private final String text;

    LicenseItem(String name, String text) {
        this.name = name;
        this.text = text;
    }

    public String getName() {
        return name;
    }

    public String getText() {
        return text;
    }

    @Override
    public String toString() {
        return name;
    }
}
